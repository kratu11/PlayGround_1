package com.ultralesson.Javapg2;

public class printingTable {

    public static void main(String[] args) {
        System.out.println("Table of 24 :");
        for(int i=1;i<=10;i++) {
            System.out.println("24*" +i+" :"+ 24*i);
        }

        System.out.println("Table of 50 :");
        for(int i=1;i<=10;i++) {
            System.out.println("50*" +i+" :"+ 50*i);
        }

        System.out.println("Table of 29 :");
        for(int i=1;i<=10;i++) {
            System.out.println("29*" +i+" :"+ 29*i);
        }

    }
}
